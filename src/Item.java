public class Item {
    String itemName;
    int quantity;
    double price;
    //constructor  of the Item class
    public Item(String itemName, int quantity) {
        this.itemName = itemName;
        this.quantity = quantity;
        this.price = price;
    }


    public void setPrice(int price) {

        this.price = price;
    }

}

